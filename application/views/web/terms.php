        ① 회원은 이 약관과는 별도로 공급계약서 및 기타 개별계약(이하 총칭하여 “개별계약”)을 체결하고, 그에 따라 정산주체에 대한 자신의 정산금채권을 회사에 양도하며, 회사로부터 양수도대금을 정산(예정일)보다 미리 지급받습니다.
        ② 회사는 회원에게 서비스를 통한 지급내용 및 기타 거래에 관한 제반사항을 빠짐없이 회사홈페이지(brandbank.biz) 또는 기타 장치를 이용해 명시하고 회원이 서비스 및 거래에 관한 모든 정보를 알 수 있도록 그 내용을 제공하여야 합니다. 단 기밀을 요하거나 회사 내부에 관련된 정보는 제외됩니다.
        ③ 회사는 구체적인 권리, 의무를 정하기 위하여 회원과 개별계약을 체결하며, 추가 증빙자료 제출을 요청할 수 있습니다.
        ④ 개인사업자 기준, 만 20세 미만이거나 만 75세 이상인 경우 선정산 서비스 이용이 불가하며, 회원가입만 가능합니다. 만 20세 미만은 성년이 된 후 선정산 서비스 이용이 가능합니다.
        ⑤ 회사는 서비스에 따라 정산수령 방식이나 광고비 등의 정산금채권 상계항목처리는 회사가 지정한 처리방식으로 요청할 수 있습니다.
