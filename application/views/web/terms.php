        ① 이 약관에서 사용하는 용어의 정의는 다음과 같습니다.
        1. “선정산서비스”라 함은 회사가 회원의 상품을 정산주체에 판매하여 발생되는 정산금을 회사가 정산(예정)일보다 먼저 지급하는 것을 뜻하며, 회원은 회사에 회원의 상품을 공급하고 회사는 약정된 수수료를 제외한 정산금의 일부를 선정산서비스를 통하여 받을 수 있도록 함.
        2. “회원”이라 함은 서비스를 이용하기 위하여 이 약관을 포함한 회사의 관련 약관을 승인함으로써 회사와 서비스 이용계약을 체결한 자를 말하며, 정산(예정)일에 회사로부터 정산금을 결제 받는 채권자를 말합니다.
        3. “정산주체”라 함은 회원의 상품을 소비자에게 판매할 수 있도록 TV 또는 온라인상에 거래공간, 서비스 등을 제공하는 홈쇼핑사업자, 통신판매업자, 통신판매중개업자(오픈마켓, 소셜커머스, 종합몰, 전문몰 등)를 포함하여, 신용카드 등의 결제 및 정산을 대행하는 전자지급결제대행사 등을 말하며, 정산(예정)일에 회사에게 정산금을 지급하는 채무자를 말합니다.
        4. “정산금”이라 함은 회사가 소비자에게 판매한 재화 등 판매대금에서 정산주체의 각종수수료, 서비스이용료 등을 공제한 나머지 금액을 말합니다.
        5. “정산(예정)일”이라 함은 정산주체와 회사 간 계약에 의하여 정산주체가 회사에게 정산금을 지급해야 하는 날을 말합니다.
        6. “양수도대금”이라 함은 회원의 채권 양수요청에 따라 회사가 회원의 정산금채권을 양수하고, 회사가 회원에게 정산(예정)일보다 앞서 지급하는 양수도대가를 말합니다.
        7. “정산금채권”이라 함은 정산주체가 회원간 계약에 의하여 회원에게 지급할 정산금에 대한 채권을 말합니다.
        8. “접근매체”라 함은 서비스 이용에 있어서 공급계약을 하거나 회원 및 거래내용의 진정성을 확보하기 위하여 사용되는 수단 또는 정보로서 전자서명법에 따른 전자서명생성정보 또는 공인인증서, 회사에 등록된 회원번호, 이상의 수단이나 정보를 사용하는데 필요한 비밀번호, 기타 회사와 회원이 합의한 방식에 의한 전자적 정보 등을 말합니다.
        10. “전자적 장치”라 함은 서비스 이용에 사용되는 컴퓨터 등의 장치를 말합니다.
        ② 이 약관에서 별도로 정하지 아니한 용어는 관계법령 및 서비스별 안내에서 정하는 바에 따르며, 그 외에는 일반 거래관행에 따릅니다.
