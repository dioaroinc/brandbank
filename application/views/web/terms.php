        ② 회원이 소비자에게 재화 등을 인도 또는 제공함으로써 구체적으로 발생하여 정산주체를 통하여 확정된 정산금에 대한 권리는 이 약관 및 개별계약에 의한 채권양도에 따라 회사에게 완전히 이전됩니다.
        ③ 회사는 이 약관 및 개별계약에 따라 정산금채권 양수 후 회원에게 양수도대금을 지급하고, 양수한 정산금채권에 근거하여 정산주체로부터 정산금을 직접 지급받거나 회원 명의의 정산금 수령계좌로부터 해당 정산금을 집금이체합니다. 정산주체가 회원 명의의 정산금 수령계좌로 정산금을 지급한 경우 회원은 CMS(Cash Management Service) 자동이체 또는 기타 이와 유사한 방법 등 회사가 미리 회원에게 고지한 방법에 따라 정산금채권의 양수인인 회사가 이를 수취할 수 있도록 해야 합니다.
        ④ 회사는 제3항에 따라 정산금을 집금이체할 때, 서비스 이용에 따른 이용수수료를 합산하여 지급받거나 집금이체할 수 있습니다. 이용수수료는 별도 안내에 따라 정산주체별 이용금액을 기준으로 한 수수료율이 적용되며, 회원에 따라 다르게 적용될 수 있습니다. 이에 회사는 이용수수료에 대한 변경사항이 발생했을 시 빠른 시일 내에 지체없이 회원에게 안내합니다.
        ⑤ 회사가 제3항에 따라 수령한 정산금이 회원에게 기지급된 양수도대금과 이용수수료 합계액을 초과하는 경우, 초과한 금원은 회원에게 반환합니다.
        ⑥ 회사에 양도된 정산금채권과 관련하여 정산주체가 회원 명의의 정산금 수령계좌로 정산금을 지급하는 경우, 회원은 수취한 정산금을 회사에 완전히 지급할 때까지 회사의 정산금 회수업무에 관한 수탁자로서 책임을 이행하여야 합니다. 회원은 선량한 관리자의 주의의무로 정산금을 보관하여야 하고, 임의로 자신의 채권과 상계하거나, 회사가 정산금을 집금이체하기 전에 이를 출금 또는 사용할 수 없습니다. 회원이 본 항에 규정된 의무를 위반하는 경우 그로 인하여 회사에게 발생한 일체의 손해를 배상하여야 합니다.
